package unicro.entity;

/**
 *
 * @author Admin
 */
public class Role {

    private int id;
    private String name;
    private Boolean active;

    public Role() {
    }

    public Role(int id, String name, Boolean active) {
        this.id = id;
        this.name = name;
        this.active = active;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Boolean getActive() {
        return active;
    }

    public void setActive(Boolean active) {
        this.active = active;
    }

}
